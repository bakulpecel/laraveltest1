<?php

namespace App\Http\Controllers;

use App\Tag;
use App\TagThread;
use App\Thread;
use App\Http\Requests\ThreadRequest;
use App\Jobs\StoreThread;
use Illuminate\Http\Request;

class ThreadController extends Controller
{
    public function __construct()
    {
        $this->middleware('auth')->only([
            'create', 'store', 'edit', 'update', 'destroy',
        ]);
    }

    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $threads = $request->search
            ? Thread::search($request->search)
            : Thread::latestPost();

        return view('forum.index', compact('threads'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('forum.threads.create', [
            'tags' => Tag::all()
        ]);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(ThreadRequest $request)
    {
        $thread = $this->dispatchNow(StoreThread::fromRequest($request));

        return redirect()
            ->route('thread.show', ['forum' => $thread->slug]);
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Thread  $thread
     * @return \Illuminate\Http\Response
     */
    public function show(Thread $thread)
    {
        return view('forum.threads.show', compact('thread'));
    }

